#!/usr/bin/env python3
"""
This module defines a type-annotated function `add` that takes two float 
arguments and returns their sum as a float.
"""

def add(a: float, b: float) -> float:
    """
    Compute the sum of two floating-point numbers.

    Parameters:
    a (float): The first number.
    b (float): The second number.

    Returns:
    float: The sum of a and b.
    """
    return a + b
