#!/usr/bin/env python3
"""
This module defines a type-annotated function `to_str` that converts a 
floating-point number to its string representation.
"""

def to_str(n: float) -> str:
    """
    Convert a floating-point number to its string representation.

    Parameters:
    n (float): The floating-point number to convert.

    Returns:
    str: The string representation of the input float.
    """
    return str(n)
